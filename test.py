from PIL import Image
import io
import struct


def display_bmp_from_float_list(float_list):
    # 将浮点数列表转换为整数列表
    int_list = [int(value) for value in float_list]

    # 将整数列表转换为字节流
    bmp_data = b"".join(struct.pack("B", value) for value in int_list)

    # 使用PIL库打开BMP图像
    image = Image.open(io.BytesIO(bmp_data))

    # 显示图像
    image.show()


